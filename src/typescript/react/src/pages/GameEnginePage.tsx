/**
 * GameEnginePage — Pure C/WASM viewport
 *
 * React serves as a minimal container only. All viewport sizing, scaling,
 * and responsive behavior is handled directly by the WASM engine from C code.
 * No Tailwind/CSS styling constraints on the primary canvas.
 *
 * Future: HUD overlays (menu, status, controls) scaffold separately if needed.
 */
export function GameEnginePage() {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const viewportRef = useRef<HTMLDivElement>(null);
  const moduleRef = useRef<BananaEngineModule | null>(null);
  const intervalRef = useRef<number>(0);
  const scriptRef = useRef<HTMLScriptElement | null>(null);
  const websocketRef = useRef<WebSocket | null>(null);
  const outboundSequenceRef = useRef(0);
  const outboundTickRef = useRef(0);
  const netcodeRef = useRef<NetcodeDomain | null>(null);
  const predictionRef = useRef<PredictionDomain | null>(null);
  const replicationRef = useRef<ReplicationDomain | null>(null);
  const renderedRemotePlayerIdsRef = useRef<Set<string>>(new Set());

  /* GameEngineUIService manages all UI state (Phase 2 integration) */
  const uiServiceRef = useRef<GameEngineUIService | null>(null);
  const keyDownHandlerRef = useRef<((event: KeyboardEvent) => void) | null>(null);
  const keyUpHandlerRef = useRef<((event: KeyboardEvent) => void) | null>(null);
  const blurHandlerRef = useRef<(() => void) | null>(null);
  const visibilityHandlerRef = useRef<(() => void) | null>(null);
  const touchStartHandlerRef = useRef<((event: TouchEvent) => void) | null>(null);
  const touchMoveHandlerRef = useRef<((event: TouchEvent) => void) | null>(null);
  const touchEndHandlerRef = useRef<((event: TouchEvent) => void) | null>(null);
  const pointerDownHandlerRef = useRef<((event: PointerEvent) => void) | null>(null);
  const pointerMoveHandlerRef = useRef<((event: PointerEvent) => void) | null>(null);
  const pointerUpHandlerRef = useRef<((event: PointerEvent) => void) | null>(null);
  const activePointerIdRef = useRef<number | null>(null);
  const touchStartTimerRef = useRef<number>(0);
  const renderHealthTimerRef = useRef<number>(0);
  const activeTouchIdRef = useRef<number | null>(null);

  /* React state subscribed from GameEngineUIService */
  const [status, setStatus] = useState<EngineStatus>("idle");
  const [error, setError] = useState<string | null>(null);
  const [contextMenu, setContextMenu] = useState<ContextMenuState>({
    visible: false,
    x: 0,
    y: 0,
    normalizedX: 0,
    normalizedY: 0,
  });
  const [radialControl, setRadialControl] = useState<RadialControlState>({
    visible: false,
    x: 0,
    y: 0,
    activeDirection: null,
  });
  const [interactionMessage, setInteractionMessage] = useState<string | null>(null);
  const [engineAssetVersion, setEngineAssetVersion] = useState<string>("pending");
  const [sessionBootstrap, setSessionBootstrap] = useState<GameSessionBootstrap | null>(null);
  const [sessionPlayers, setSessionPlayers] = useState<GameSessionPlayer[]>([]);
  const [localPlayerId, setLocalPlayerId] = useState<string | null>(null);
  const localPlayerIdRef = useRef<string | null>(null);
  const [snapshotEntities, setSnapshotEntities] = useState<Record<string, GameSessionSnapshotEntity>>(
    {}
  );
  const playerRosterSignatureRef = useRef("");
  const [connectionState, setConnectionState] = useState<"connecting" | "connected" | "disconnected">(
    "disconnected"
  );
  const [serverTick, setServerTick] = useState(0);
  const [serverMetrics, setServerMetrics] = useState<GameSessionServerMetrics | null>(null);
  const [nativeActivePlayerCount, setNativeActivePlayerCount] = useState(0);
  const [buildStats, setBuildStats] = useState<GameplayBuildStats | null>(null);
  const [comboResult, setComboResult] = useState<GameplayComboResult | null>(null);
  const [buildBusy, setBuildBusy] = useState(false);
  const buildBusyRef = useRef(false);
  const [localPlayerWorldPosition, setLocalPlayerWorldPosition] = useState<WorldPosition | null>(null);
  const [pbjPickupAvailable, setPbjPickupAvailable] = useState(true);
  const [bananaParticles, setBananaParticles] = useState<DancingBananaParticle[]>([]);
  const particleIdRef = useRef(0);
  const particleClearTimerRef = useRef<number>(0);

  const applySessionPlayers = (players: GameSessionPlayer[]) => {
    const nextPlayers = players ?? [];
    const signature = nextPlayers
      .map((player) => `${player.playerId}:${player.playerName}:${player.connected ? 1 : 0}`)
      .join("|");

    if (signature === playerRosterSignatureRef.current) {
      return;
    }

    playerRosterSignatureRef.current = signature;
    setSessionPlayers(nextPlayers);
  };

  const spawnDancingBananaParticles = () => {
    const glyphs = ["🍌", "🕺", "✨", "🎶"];
    const particles: DancingBananaParticle[] = Array.from({ length: 28 }, (_, index) => ({
      id: particleIdRef.current + index,
      leftPct: 38 + Math.random() * 24,
      topPct: 42 + Math.random() * 20,
      durationMs: 1200 + Math.floor(Math.random() * 2200),
      delayMs: Math.floor(Math.random() * 380),
      sizeRem: 1 + Math.random() * 1.2,
      glyph: glyphs[Math.floor(Math.random() * glyphs.length)] ?? "🍌",
    }));

    particleIdRef.current += particles.length;
    setBananaParticles(particles);
    window.clearTimeout(particleClearTimerRef.current);
    particleClearTimerRef.current = window.setTimeout(() => {
      setBananaParticles([]);
    }, 4800);
  };

  useEffect(() => {
    if (!pbjPickupAvailable || !localPlayerWorldPosition) {
      return;
    }

    const pickupDistance = Math.hypot(
      localPlayerWorldPosition.x - PBJ_PICKUP_WORLD_POSITION.x,
      localPlayerWorldPosition.z - PBJ_PICKUP_WORLD_POSITION.z
    );

    if (pickupDistance > PBJ_PICKUP_COLLECT_RADIUS) {
      return;
    }

    setPbjPickupAvailable(false);
    setInteractionMessage("Peanut Butter Jelly Time! Dancing banana particles unleashed.");
    spawnDancingBananaParticles();
  }, [pbjPickupAvailable, localPlayerWorldPosition]);

  useEffect(() => {
    try {
      const stored = sessionStorage.getItem(GAME_SESSION_STORAGE_KEY);
      if (!stored) {
        routeToSessionRoom(true);
        return;
      }

      const parsed = JSON.parse(stored) as GameSessionBootstrap;
      if (!parsed.sessionId || !parsed.token || !parsed.playerName) {
        sessionStorage.removeItem(GAME_SESSION_STORAGE_KEY);
        routeToSessionRoom(true);
        return;
      }

      const normalized: GameSessionBootstrap = {
        ...parsed,
        playerGuid: parsed.playerGuid || resolveStablePlayerGuid(),
      };

      sessionStorage.setItem(GAME_SESSION_STORAGE_KEY, JSON.stringify(normalized));

      setSessionBootstrap(normalized);
    } catch {
      sessionStorage.removeItem(GAME_SESSION_STORAGE_KEY);
      routeToSessionRoom(true);
    }
  }, []);

  useEffect(() => {
    localPlayerIdRef.current = localPlayerId;
  }, [localPlayerId]);

  useEffect(() => {
    if (!sessionBootstrap) return;

    const resolutionError = resolveApiBaseResolutionError();
    if (resolutionError) {
      setError(resolutionError);
      return;
    }

    const websocketUrl = resolveGameSessionWebSocketUrl(
      resolveApiBaseUrl(),
      sessionBootstrap.sessionId,
      sessionBootstrap.playerName,
      sessionBootstrap.playerGuid
    );
    const socket = new WebSocket(websocketUrl);

    websocketRef.current = socket;
    setConnectionState("connecting");

    socket.addEventListener("open", () => {
      setConnectionState("connected");
    });

    socket.addEventListener("close", () => {
      if (websocketRef.current === socket) {
        websocketRef.current = null;
      }
      setConnectionState("disconnected");
    });

    socket.addEventListener("error", () => {
      setConnectionState("disconnected");
      setError("Realtime session connection failed. Rejoin the room and retry.");
    });

    socket.addEventListener("message", (event) => {
      if (typeof event.data !== "string") {
        return;
      }

      try {
        const payload = JSON.parse(event.data) as
          | GameSessionSnapshotEnvelope
          | {
            type: "connected";
            playerId?: string;
            players?: GameSessionPlayer[];
            lastSequence?: number;
            lastTick?: number;
          }
          | { type: "ack"; sequence?: number; tick?: number }
          | { type: "pong"; ts?: number }
          | {
            type: "error";
            error?: string;
            detail?: string;
            expectedSequence?: number;
            expectedTick?: number;
          };

        if (payload.type === "connected") {
          setLocalPlayerId(payload.playerId ?? null);
          localPlayerIdRef.current = payload.playerId ?? null;
          if (Number.isFinite(payload.lastSequence)) {
            outboundSequenceRef.current = Math.max(outboundSequenceRef.current, Number(payload.lastSequence));
          }
          if (Number.isFinite(payload.lastTick)) {
            outboundTickRef.current = Math.max(outboundTickRef.current, Number(payload.lastTick));
          }
          applySessionPlayers(payload.players ?? []);
          return;
        }

        if (payload.type === "error") {
          const detail = payload.detail ?? "Realtime session reported an error.";
          if (payload.error === "invalid_input") {
            const nextSequence = Number(payload.expectedSequence ?? outboundSequenceRef.current);
            const nextTick = Number(payload.expectedTick ?? outboundTickRef.current);
            if (Number.isFinite(nextSequence)) {
              outboundSequenceRef.current = Math.max(outboundSequenceRef.current, nextSequence);
            }
            if (Number.isFinite(nextTick)) {
              outboundTickRef.current = Math.max(outboundTickRef.current, nextTick);
            }
            setInteractionMessage(`Realtime input resynced (${detail}); continuing.`);
            return;
          }

          setError(detail);
          return;
        }

        if (payload.type === "ack" || payload.type === "pong") {
          return;
        }

        if (payload.type !== "snapshot") {
          return;
        }

        applySessionPlayers(payload.players ?? []);
        setSnapshotEntities(payload.snapshot.entities ?? {});
        setServerTick(payload.snapshot.tick);
        setServerMetrics(payload.server ?? null);

        const module = moduleRef.current;
        if (module) {
          const remotePlayerIds = new Set<string>();
          const playersById = new Map((payload.players ?? []).map((player) => [player.playerId, player]));

          Object.values(payload.snapshot.entities ?? {}).forEach((entity) => {
            if (entity.type !== "player") return;
            const playerId = (entity.playerGuid ?? entity.ownerPlayerId ?? "").trim().toLowerCase();
            if (!playerId) return;
            if (playerId === localPlayerIdRef.current) return;

            remotePlayerIds.add(playerId);
            const rosterPlayer = playersById.get(playerId);

            module.ccall(
              "engine_player_upsert",
              "number",
              ["string", "string", "string", "number"],
              [
                playerId,
                entity.ownerPlayerName ?? rosterPlayer?.playerName ?? playerId,
                entity.controllerKind ?? rosterPlayer?.controllerKind ?? "human",
                entity.active ? 1 : 0,
              ]
            );

            module.ccall(
              "engine_player_set_transform",
              "null",
              ["string", "number", "number", "number", "number"],
              [playerId, entity.x, entity.y, entity.z, entity.active ? 1 : 0]
            );
          });

          renderedRemotePlayerIdsRef.current.forEach((playerId) => {
            if (remotePlayerIds.has(playerId)) return;
            module.ccall(
              "engine_player_upsert",
              "number",
              ["string", "string", "string", "number"],
              [playerId, playerId, "human", 0]
            );
          });

          renderedRemotePlayerIdsRef.current = remotePlayerIds;
        }

        replicationRef.current?.applyAuthoritativeState(payload.snapshot);

        const localPlayer =
          (localPlayerIdRef.current
            ? Object.values(payload.snapshot.entities).find(
              (entity) => entity.ownerPlayerId === localPlayerIdRef.current
            )
            : undefined) ??
          payload.snapshot.entities["entity-1"] ??
          Object.values(payload.snapshot.entities).find((entity) => entity.type === "player");

        if (!localPlayer) {
          return;
        }

        setLocalPlayerWorldPosition({ x: localPlayer.x, z: localPlayer.z });

        predictionRef.current?.reconcileAndReplay({
          tickIndex: payload.snapshot.tick,
          positionX: localPlayer.x,
          positionZ: localPlayer.z,
          timestamp: payload.snapshot.timestamp,
        });

        netcodeRef.current?.reconcileAuthoritativeState(payload.snapshot.tick, {
          tickIndex: payload.snapshot.tick,
          moveX: 0,
          moveZ: 0,
          timestamp: payload.snapshot.timestamp,
          isAuthoritative: true,
        });
      } catch {
        setInteractionMessage("Realtime snapshot parse warning; waiting for next server update.");
      }
    });

    return () => {
      window.clearTimeout(particleClearTimerRef.current);
      if (websocketRef.current === socket) {
        websocketRef.current = null;
      }
      renderedRemotePlayerIdsRef.current = new Set();
      socket.close();
    };
  }, [sessionBootstrap]);

  const loadBuildStats = async () => {
    if (!sessionBootstrap) return;
    try {
      const stats = await fetchPlayerBuildStats(
        resolveApiBaseUrl(),
        sessionBootstrap.sessionId,
        sessionBootstrap.playerGuid
      );
      setBuildStats(stats);
    } catch (error) {
      setInteractionMessage(
        `Build stats load failed: ${error instanceof Error ? error.message : String(error)}`
      );
    }
  };

  const withBuildAction = async (action: () => Promise<void>) => {
    if (!sessionBootstrap || buildBusyRef.current) return;
    buildBusyRef.current = true;
    setBuildBusy(true);
    try {
      await action();
      await loadBuildStats();
    } catch (error) {
      setInteractionMessage(
        `Build action failed: ${error instanceof Error ? error.message : String(error)}`
      );
    } finally {
      buildBusyRef.current = false;
      setBuildBusy(false);
    }
  };

  const handleSetClass = (classType: 0 | 1 | 2) => {
    void withBuildAction(async () => {
      if (!sessionBootstrap) return;
      await setPlayerBuildClass(
        resolveApiBaseUrl(),
        sessionBootstrap.sessionId,
        sessionBootstrap.playerGuid,
        classType
      );
      const classLabel = classType === 0 ? "Vanguard" : classType === 1 ? "Arcanist" : "Ranger";
      setInteractionMessage(`Class updated (${classLabel}).`);
    });
  };

  const handleApplyPreset = () => {
    void withBuildAction(async () => {
      if (!sessionBootstrap) return;
      await setPlayerBuildAllocations(
        resolveApiBaseUrl(),
        sessionBootstrap.sessionId,
        sessionBootstrap.playerGuid,
        3,
        1,
        2
      );
      setInteractionMessage("Applied build allocation preset 3/1/2.");
    });
  };

  const handleEquipGear = () => {
    void withBuildAction(async () => {
      if (!sessionBootstrap) return;
      await equipPlayerBuildGear(
        resolveApiBaseUrl(),
        sessionBootstrap.sessionId,
        sessionBootstrap.playerGuid,
        0,
        2,
        4,
        0,
        0
      );
      setInteractionMessage("Equipped Tier-2 weapon modifier.");
    });
  };

  const handleEvaluateCombo = () => {
    void withBuildAction(async () => {
      if (!sessionBootstrap) return;
      const result = await evaluatePlayerBuildCombo(
        resolveApiBaseUrl(),
        sessionBootstrap.sessionId,
        sessionBootstrap.playerGuid,
        "snare_shot",
        "piercing_volley",
        1000,
        Math.max(1, sessionPlayers.length || 1)
      );
      setComboResult(result);
      setInteractionMessage(
        result.triggered
          ? `Combo triggered (+${result.damageBonusPct}% dmg).`
          : "Combo did not trigger."
      );
    });
  };

  useEffect(() => {
    if (!sessionBootstrap) return;
    void loadBuildStats();
  }, [sessionBootstrap]);

  useLayoutEffect(() => {
    const canvas = canvasRef.current;
    const viewport = viewportRef.current;
    if (!canvas || !viewport) return;

    // Native WASM window path resolves the target by #canvas.
    // biome-ignore lint: useUniqueElementIds: Emscripten native code hardcodes #canvas.
    canvas.id = "canvas";

    const resolveViewportSize = () => {
      const bounds = viewport.getBoundingClientRect();
      const visualViewport = window.visualViewport;

      // On some mobile browsers during chrome/url-bar transitions, fixed containers
      // can briefly report 0x0. Fall back to visualViewport/window dimensions.
      const cssWidth = Math.max(
        1,
        Math.floor(
          (bounds.width > 1 ? bounds.width : 0) ||
          visualViewport?.width ||
          window.innerWidth ||
          document.documentElement.clientWidth ||
          1
        )
      );
      const cssHeight = Math.max(
        1,
        Math.floor(
          (bounds.height > 1 ? bounds.height : 0) ||
          visualViewport?.height ||
          window.innerHeight ||
          document.documentElement.clientHeight ||
          1
        )
      );

      return { cssWidth, cssHeight };
    };

    const syncCanvasCssSize = () => {
      const { cssWidth, cssHeight } = resolveViewportSize();
      const dpr = Math.max(1, window.devicePixelRatio || 1);
      const pixelWidth = Math.max(1, Math.floor(cssWidth * dpr));
      const pixelHeight = Math.max(1, Math.floor(cssHeight * dpr));
      canvas.style.width = `${cssWidth}px`;
      canvas.style.height = `${cssHeight}px`;
      if (canvas.width !== pixelWidth) {
        canvas.width = pixelWidth;
      }
      if (canvas.height !== pixelHeight) {
        canvas.height = pixelHeight;
      }
    };

    syncCanvasCssSize();

    const observer = new ResizeObserver(() => {
      syncCanvasCssSize();
    });
    observer.observe(viewport);

    window.addEventListener("resize", syncCanvasCssSize);
    window.visualViewport?.addEventListener("resize", syncCanvasCssSize);
    return () => {
      observer.disconnect();
      window.removeEventListener("resize", syncCanvasCssSize);
      window.visualViewport?.removeEventListener("resize", syncCanvasCssSize);
    };
  }, []);

  useEffect(() => {
    const env = (import.meta as { env?: Record<string, unknown> }).env;
    const isDev = Boolean(env?.DEV);
    if (!isDev) return;
    if (window.location.hostname === "app.example") return;

    let lastVersion: string | null = null;
    const pollForWasmChanges = async () => {
      try {
        const response = await fetch(`/wasm/engine.version.json?dev-watch=${Date.now()}`, {
          cache: "no-store",
        });
        if (!response.ok) return;

        const payload = (await response.json()) as { engineAssetVersion?: string };
        const currentVersion = payload.engineAssetVersion;
        if (!currentVersion) return;

        if (lastVersion === null) {
          lastVersion = currentVersion;
          return;
        }

        if (currentVersion !== lastVersion) {
          window.location.reload();
        }
      } catch {
        /* best-effort dev polling; ignore transient fetch errors */
      }
    };

    const watcher = window.setInterval(() => {
      void pollForWasmChanges();
    }, 3000);

    return () => {
      window.clearInterval(watcher);
    };
  }, []);

  useEffect(() => {
    if (window.location.hostname === "app.example") {
      setEngineAssetVersion(resolveFallbackEngineVersion());
      return;
    }

    let canceled = false;

    const loadEngineVersion = async () => {
      try {
        const response = await fetch(`/wasm/engine.version.json?meta=${Date.now()}`, {
          cache: "no-store",
        });
        if (!response.ok) {
          if (!canceled) setEngineAssetVersion(resolveFallbackEngineVersion());
          return;
        }
        const payload = (await response.json()) as { engineAssetVersion?: string };
        if (!canceled && payload.engineAssetVersion) {
          setEngineAssetVersion(payload.engineAssetVersion);
        } else if (!canceled) {
          setEngineAssetVersion(resolveFallbackEngineVersion());
        }
      } catch {
        if (!canceled) {
          setEngineAssetVersion(resolveFallbackEngineVersion());
        }
      }
    };

    void loadEngineVersion();
    return () => {
      canceled = true;
    };
  }, []);

  /* C/WASM engine handles all asset rendering */

  const runContextAction = (actionId: string) => {
    const mod = moduleRef.current;
    const svc = uiServiceRef.current;
    if (!mod || !svc) {
      setInteractionMessage("Action unavailable: engine not ready.");
      if (svc) svc.closeContextMenu();
      return;
    }

    const interactionMgr = svc.getInteractionManager();
    const menuState = interactionMgr.getMenuState();

    const result = Number(
      mod.ccall(
        "engine_handle_right_click_normalized",
        "number",
        ["number", "number"],
        [menuState.normalizedX, menuState.normalizedY]
      )
    );

    if (result === 1) {
      setInteractionMessage(`${actionId}: applied to nearest actor.`);
    } else {
      setInteractionMessage(`${actionId}: no target in range.`);
    }
    svc.closeContextMenu();
  };

  useEffect(() => {
    if (engineAssetVersion === "pending") return;

    const canvas = canvasRef.current;
    const viewport = viewportRef.current;
    if (!canvas || !viewport) return;

    const hasPreloadedEngineFactory = typeof window.BananaEngine === "function";
    if (!hasPreloadedEngineFactory && !isWebGL2Available()) {
      setStatus("unavailable");
      setError(
        "This browser/device cannot create a WebGL2 context. Open on a WebGL2-capable browser/device or enable hardware acceleration."
      );
      return;
    }

    setStatus("loading");

    const startEngine = async () => {
      if (!window.BananaEngine) {
        setStatus("error");
        setError("BananaEngine factory not found after script load");
        return;
      }

      /* Phase 2 Integration: Create GameEngineUIService (manages all UI state) */
      const uiService = new GameEngineUIService(viewport, CONTEXT_MENU_ACTIONS);
      uiServiceRef.current = uiService;

      // Phase 3: initialize multiplayer-ready domains behind the existing UI service.
      if (!netcodeRef.current) {
        netcodeRef.current = new NetcodeDomain(
          { tickRate: 30, maxPrediction: 60 },
          uiService.getInputAggregator(),
          uiService.getInteractionManager()
        );
      }
      if (!predictionRef.current) {
        predictionRef.current = new PredictionDomain(new DeterministicLocalSimulator(), 120);
      }
      if (!replicationRef.current) {
        replicationRef.current = new ReplicationDomain({ maxHistory: 120 });
      }

      /* Subscribe to state changes from service */
      const unsubscribeStateChanges = uiService.onStateChange((uiState: GameEngineUIState) => {
        setStatus(uiState.engineStatus);
        setError(uiState.error ?? null);

        /* Update context menu state from interaction manager */
        const interactionMgr = uiService.getInteractionManager();
        const menuState = interactionMgr.getMenuState();
        setContextMenu(menuState);

        /* Update interaction message from service */
        setInteractionMessage(uiState.interactionMessage ?? null);
      });

      /* Register action handlers with interaction domain */
      const interactionMgr = uiService.getInteractionManager();
      interactionMgr.registerActionHandler("interact", (actionId, normalizedX, normalizedY) => {
        const mod = moduleRef.current;
        if (!mod) {
          return {
            actionId,
            targetNormalizedX: normalizedX,
            targetNormalizedY: normalizedY,
            success: false,
            message: "Engine not ready",
            timestamp: Date.now(),
          };
        }
        const result = Number(
          mod.ccall(
            "engine_handle_right_click_normalized",
            "number",
            ["number", "number"],
            [normalizedX, normalizedY]
          )
        );
        return {
          actionId,
          targetNormalizedX: normalizedX,
          targetNormalizedY: normalizedY,
          success: result === 1,
          message:
            result === 1 ? "Interact: applied to nearest actor." : "Interact: no target in range.",
          timestamp: Date.now(),
        };
      });

      interactionMgr.registerActionHandler("harvest", (actionId, normalizedX, normalizedY) => {
        const mod = moduleRef.current;
        if (!mod) {
          return {
            actionId,
            targetNormalizedX: normalizedX,
            targetNormalizedY: normalizedY,
            success: false,
            message: "Engine not ready",
            timestamp: Date.now(),
          };
        }
        const result = Number(
          mod.ccall(
            "engine_handle_right_click_normalized",
            "number",
            ["number", "number"],
            [normalizedX, normalizedY]
          )
        );
        return {
          actionId,
          targetNormalizedX: normalizedX,
          targetNormalizedY: normalizedY,
          success: result === 1,
          message:
            result === 1 ? "Harvest: applied to nearest actor." : "Harvest: no target in range.",
          timestamp: Date.now(),
        };
      });

      let runtimeInitFailed = false;
      const applyRuntimeFatal = (rawMessage: unknown) => {
        const normalized = typeof rawMessage === "string" ? rawMessage : String(rawMessage ?? "");
        const message = normalized.toLowerCase();
        if (!message) return;
        const isFatalInit =
          message.includes("webgl2 context creation failed") ||
          message.includes("window_create failed") ||
          message.includes("engine_init failed") ||
          message.includes("abort");
        if (!isFatalInit) return;
        runtimeInitFailed = true;
        setInteractionMessage(`WASM runtime warning: ${normalized}`);
      };

      try {
        const mod = await window.BananaEngine({
          canvas,
          locateFile: (path, prefix) => `${prefix}${path}?v=${engineAssetVersion}`,
          printErr: (text) => {
            applyRuntimeFatal(text);
          },
          onAbort: (reason) => {
            applyRuntimeFatal(reason);
          },
        });

        if (runtimeInitFailed) {
          unsubscribeStateChanges();
          return;
        }

        moduleRef.current = mod;
        window.__bananaEngineModule = mod;

        /* Event handlers delegate to GameEngineUIService domains */
        const inputMgr = uiService.getInputAggregator();
        const _viewportMgr = uiService.getViewportSizer();

        const keyDown = (event: KeyboardEvent) => {
          const key = event.key.toLowerCase();
          if (key === "escape") {
            uiService.closeContextMenu();
            return;
          }
          if (isMovementKey(key)) {
            inputMgr.updateKeyState(key, true);
            event.preventDefault();
            uiService.closeContextMenu();
          }
        };

        const keyUp = (event: KeyboardEvent) => {
          const key = event.key.toLowerCase();
          if (isMovementKey(key)) {
            inputMgr.updateKeyState(key, false);
          }
        };

        const clearMovementIntent = () => {
          inputMgr.updateKeyState("w", false);
          inputMgr.updateKeyState("a", false);
          inputMgr.updateKeyState("s", false);
          inputMgr.updateKeyState("d", false);
          inputMgr.updateRadialDirection(null);
          mod.ccall("engine_set_move_input", "null", ["number", "number"], [0, 0]);
          setRadialControl({ visible: false, x: 0, y: 0, activeDirection: null });
        };

        const onBlur = () => {
          clearMovementIntent();
        };

        const onVisibilityChange = () => {
          if (document.hidden) {
            clearMovementIntent();
          }
        };

        /* Touch handlers for mobile radial movement control */
        const onTouchStart = (event: TouchEvent) => {
          if (event.touches.length === 0) return;

          const touch = event.touches[0];
          activeTouchIdRef.current = touch.identifier;

          const bounds = viewport.getBoundingClientRect();
          const touchX = bounds ? touch.clientX - bounds.left : touch.clientX;
          const touchY = bounds ? touch.clientY - bounds.top : touch.clientY;

          touchStartTimerRef.current = window.setTimeout(() => {
            setRadialControl({
              visible: true,
              x: touchX,
              y: touchY,
              activeDirection: null,
            });
          }, 150);
        };

        const onTouchMove = (event: TouchEvent) => {
          if (activeTouchIdRef.current === null) return;

          const touch = Array.from(event.touches).find(
            (t) => t.identifier === activeTouchIdRef.current
          );
          if (!touch) return;

          const bounds = viewport.getBoundingClientRect();
          const touchX = bounds ? touch.clientX - bounds.left : touch.clientX;
          const touchY = bounds ? touch.clientY - bounds.top : touch.clientY;

          setRadialControl((prev) => {
            if (!prev.visible) return prev;

            const direction = getDirectionFromTouch(touchX, touchY, prev.x, prev.y);

            if (direction !== prev.activeDirection) {
              inputMgr.updateRadialDirection(direction);
            }

            return { ...prev, activeDirection: direction };
          });
        };

        const onTouchEnd = (event: TouchEvent) => {
          const touch = Array.from(event.changedTouches).find(
            (t) => t.identifier === activeTouchIdRef.current
          );
          if (!touch) return;

          window.clearTimeout(touchStartTimerRef.current);
          activeTouchIdRef.current = null;
          inputMgr.updateRadialDirection(null);
          setRadialControl({ visible: false, x: 0, y: 0, activeDirection: null });
        };

        const onPointerDown = (event: PointerEvent) => {
          if (event.pointerType === "mouse") return;

          activePointerIdRef.current = event.pointerId;

          const bounds = viewport.getBoundingClientRect();
          const touchX = bounds ? event.clientX - bounds.left : event.clientX;
          const touchY = bounds ? event.clientY - bounds.top : event.clientY;

          window.clearTimeout(touchStartTimerRef.current);
          touchStartTimerRef.current = window.setTimeout(() => {
            setRadialControl({
              visible: true,
              x: touchX,
              y: touchY,
              activeDirection: null,
            });
          }, 150);
        };

        const onPointerMove = (event: PointerEvent) => {
          if (activePointerIdRef.current !== event.pointerId) return;

          const bounds = viewport.getBoundingClientRect();
          const touchX = bounds ? event.clientX - bounds.left : event.clientX;
          const touchY = bounds ? event.clientY - bounds.top : event.clientY;

          setRadialControl((prev) => {
            if (!prev.visible) return prev;

            const direction = getDirectionFromTouch(touchX, touchY, prev.x, prev.y);

            if (direction !== prev.activeDirection) {
              inputMgr.updateRadialDirection(direction);
            }

            return { ...prev, activeDirection: direction };
          });
        };

        const onPointerUp = (event: PointerEvent) => {
          if (activePointerIdRef.current !== event.pointerId) return;

          window.clearTimeout(touchStartTimerRef.current);
          activePointerIdRef.current = null;
          inputMgr.updateRadialDirection(null);
          setRadialControl({ visible: false, x: 0, y: 0, activeDirection: null });
        };

        keyDownHandlerRef.current = keyDown;
        keyUpHandlerRef.current = keyUp;
        blurHandlerRef.current = onBlur;
        visibilityHandlerRef.current = onVisibilityChange;
        touchStartHandlerRef.current = onTouchStart;
        touchMoveHandlerRef.current = onTouchMove;
        touchEndHandlerRef.current = onTouchEnd;
        pointerDownHandlerRef.current = onPointerDown;
        pointerMoveHandlerRef.current = onPointerMove;
        pointerUpHandlerRef.current = onPointerUp;

        window.addEventListener("keydown", keyDown, { capture: true });
        window.addEventListener("keyup", keyUp, { capture: true });
        window.addEventListener("blur", onBlur);
        document.addEventListener("visibilitychange", onVisibilityChange);
        window.addEventListener("touchstart", onTouchStart, { passive: true });
        window.addEventListener("touchmove", onTouchMove, { passive: true });
        window.addEventListener("touchend", onTouchEnd, { passive: true });
        window.addEventListener("pointerdown", onPointerDown, { passive: true });
        window.addEventListener("pointermove", onPointerMove, { passive: true });
        window.addEventListener("pointerup", onPointerUp, { passive: true });
        window.addEventListener("pointercancel", onPointerUp, { passive: true });

        uiService.setEngineStatus("running");

        let tickCounter = 0;
        let consecutiveBlankFrameProbes = 0;
        const probeFramebuffer = () => {
          try {
            const gl = canvas.getContext("webgl2");
            if (!gl) {
              setInteractionMessage("WebGL2 probe unavailable; continuing runtime.");
              return;
            }
            const width = Math.max(1, canvas.width);
            const height = Math.max(1, canvas.height);
            const sampleAt = (x: number, y: number) => {
              const pixel = new Uint8Array(4);
              gl.readPixels(x, y, 1, 1, gl.RGBA, gl.UNSIGNED_BYTE, pixel);
              return pixel;
            };
            const samples = [
              sampleAt(Math.floor(width / 2), Math.floor(height / 2)),
              sampleAt(Math.floor(width / 4), Math.floor(height / 4)),
              sampleAt(Math.max(0, width - 2), Math.max(0, height - 2)),
            ];
            const hasAnyRenderedPixel = samples.some(
              (pixel) => pixel[0] !== 0 || pixel[1] !== 0 || pixel[2] !== 0 || pixel[3] !== 0
            );
            if (hasAnyRenderedPixel) {
              consecutiveBlankFrameProbes = 0;
              return;
            }
            consecutiveBlankFrameProbes += 1;
            if (consecutiveBlankFrameProbes >= 2) {
              setInteractionMessage(
                "Render probe detected dark frames; continuing runtime for visual iteration."
              );
              consecutiveBlankFrameProbes = 0;
            }
          } catch {
            setInteractionMessage("Render probe failed; continuing runtime.");
          }
        };

        const fpsLoop = () => {
          try {
            /* Get current movement input from service */
            const movement = inputMgr.computeMovement();
            netcodeRef.current?.advanceTick();
            const tick = netcodeRef.current?.getCurrentTick() ?? 0;
            replicationRef.current?.recordState(tick, {
              tick,
              timestamp: Date.now(),
              entities: {
                localPlayer: {
                  moveX: movement.moveX,
                  moveZ: movement.moveZ,
                  source: movement.source,
                },
              },
              isAuthoritative: false,
            });
            predictionRef.current?.predict(movement);
            if (sessionBootstrap && websocketRef.current?.readyState === WebSocket.OPEN) {
              outboundSequenceRef.current += 1;
              outboundTickRef.current += 1;
              websocketRef.current.send(
                JSON.stringify({
                  type: "input",
                  sequence: outboundSequenceRef.current,
                  tick: outboundTickRef.current,
                  moveX: movement.moveX,
                  moveZ: movement.moveZ,
                  timestamp: Date.now(),
                })
              );
            }
            mod.ccall(
              "engine_set_move_input",
              "null",
              ["number", "number"],
              [movement.moveX, movement.moveZ]
            );
            mod.ccall("engine_tick", "number", ["number"], [1 / 60]);
            tickCounter += 1;
            if (tickCounter % 15 === 0) {
              const activePlayers = Number(
                mod.ccall("engine_get_active_player_count", "number", [], [])
              );
              if (Number.isFinite(activePlayers)) {
                setNativeActivePlayerCount(activePlayers);
              }
            }
            if (tickCounter % 60 === 0) {
              probeFramebuffer();
            }
          } catch {
            setInteractionMessage(
              "Runtime tick warning encountered; continuing WASM loop."
            );
          }
        };

        intervalRef.current = window.setInterval(fpsLoop, 16);

        renderHealthTimerRef.current = window.setTimeout(() => {
          if (runtimeInitFailed) return;
          probeFramebuffer();
        }, 1200);
      } catch (e) {
        uiService.setEngineStatus(
          "error",
          e instanceof Error ? e.message : "Engine initialization failed"
        );
        unsubscribeStateChanges();
      }
    };

    if (typeof window.BananaEngine === "function") {
      void startEngine();
    } else {
      const script = document.createElement("script");
      script.src = `/wasm/engine.js?v=${engineAssetVersion}`;
      scriptRef.current = script;

      script.onerror = () => {
        setStatus("unavailable");
        setError(
          "engine.js not found. Run: bash scripts/build-engine-wasm.sh (requires Emscripten SDK)"
        );
      };

      script.onload = () => {
        void startEngine();
      };

      document.head.appendChild(script);
    }

    return () => {
      window.clearInterval(intervalRef.current);
      window.clearTimeout(touchStartTimerRef.current);
      window.clearTimeout(renderHealthTimerRef.current);

      if (keyDownHandlerRef.current) {
        window.removeEventListener("keydown", keyDownHandlerRef.current, { capture: true });
        keyDownHandlerRef.current = null;
      }
      if (keyUpHandlerRef.current) {
        window.removeEventListener("keyup", keyUpHandlerRef.current, { capture: true });
        keyUpHandlerRef.current = null;
      }
      if (blurHandlerRef.current) {
        window.removeEventListener("blur", blurHandlerRef.current);
        blurHandlerRef.current = null;
      }
      if (visibilityHandlerRef.current) {
        document.removeEventListener("visibilitychange", visibilityHandlerRef.current);
        visibilityHandlerRef.current = null;
      }
      if (touchStartHandlerRef.current) {
        window.removeEventListener("touchstart", touchStartHandlerRef.current);
        touchStartHandlerRef.current = null;
      }
      if (touchMoveHandlerRef.current) {
        window.removeEventListener("touchmove", touchMoveHandlerRef.current);
        touchMoveHandlerRef.current = null;
      }
      if (touchEndHandlerRef.current) {
        window.removeEventListener("touchend", touchEndHandlerRef.current);
        touchEndHandlerRef.current = null;
      }
      if (pointerDownHandlerRef.current) {
        window.removeEventListener("pointerdown", pointerDownHandlerRef.current);
        pointerDownHandlerRef.current = null;
      }
      if (pointerMoveHandlerRef.current) {
        window.removeEventListener("pointermove", pointerMoveHandlerRef.current);
        pointerMoveHandlerRef.current = null;
      }
      if (pointerUpHandlerRef.current) {
        window.removeEventListener("pointerup", pointerUpHandlerRef.current);
        window.removeEventListener("pointercancel", pointerUpHandlerRef.current);
        pointerUpHandlerRef.current = null;
      }

      scriptRef.current?.remove();
      delete window.__bananaEngineModule;
      moduleRef.current = null;
      uiServiceRef.current = null;
      netcodeRef.current = null;
      predictionRef.current = null;
      replicationRef.current = null;
    };
  }, [engineAssetVersion, sessionBootstrap]);

  const showSplash = status === "idle" || status === "loading";
  const uiServiceState = uiServiceRef.current?.getState() ?? null;
  const nativeUiFrame = uiServiceState
    ? marshalNativeUiFrame({
        host: "web",
        state: {
          engineStatus: status,
          error,
          movementInput: uiServiceState.movementInput,
          viewportSize: uiServiceState.viewportSize,
          interactionMessage,
          timestamp: uiServiceState.timestamp,
        },
      })
    : null;

  useEffect(() => {
    window.banana?.setNativeUiFrame?.(nativeUiFrame);

    return () => {
      window.banana?.setNativeUiFrame?.(null);
    };
  }, [nativeUiFrame]);

  return (
    <div
      ref={viewportRef}
      style={{
        position: "fixed",
        inset: 0,
        width: "100vw",
        height: "100vh",
        minHeight: "100dvh",
        margin: 0,
        padding: 0,
        overflow: "hidden",
        backgroundColor: "#000",
        touchAction: "none",
      }}
    >
      <SplashOverlay visible={showSplash} />

      <canvas
        id="canvas"
        ref={canvasRef}
        onMouseDown={() => {
          if (contextMenu.visible) {
            const svc = uiServiceRef.current;
            if (svc) svc.closeContextMenu();
          }
        }}
        onContextMenu={(event) => {
          event.preventDefault();
          const svc = uiServiceRef.current;
          if (!svc) return;

          const bounds = viewportRef.current?.getBoundingClientRect();
          const relativeX = bounds ? event.clientX - bounds.left : event.clientX;
          const relativeY = bounds ? event.clientY - bounds.top : event.clientY;

          svc.openContextMenu(relativeX, relativeY);
        }}
        style={{
          display: "block",
          width: "100%",
          height: "100%",
          imageRendering: "pixelated",
          touchAction: "none",
        }}
      />

      {/* Error overlay */}
      {(status === "error" || status === "unavailable") && error && (
        <ViewportErrorOverlay error={error} onReload={() => window.location.reload()} />
      )}

      {/* Error notification badge */}
      {(status === "error" || status === "unavailable") && error && (
        <ErrorBadge message={error} />
      )}

      {/* Controls hint */}
      <ControlsHint />

      {/* Context menu */}
      <ContextMenu
        state={contextMenu}
        actions={CONTEXT_MENU_ACTIONS}
        onAction={runContextAction}
        onClose={() => setContextMenu((prev) => ({ ...prev, visible: false }))}
      />

      {/* Mobile radial control */}
      <MobileRadialControl state={radialControl} />

      {/* Interaction message badge */}
      {interactionMessage && <InteractionMessageBadge message={interactionMessage} />}

      {/* Engine version debug badge */}
      <EngineVersionBadge version={engineAssetVersion} messagePresent={!!interactionMessage} />

      {bananaParticles.length > 0 && (
        <>
          <style>{`
            @keyframes bananaDanceBurst {
              0% { opacity: 0; transform: translate3d(0, 10px, 0) scale(0.5) rotate(0deg); }
              12% { opacity: 1; transform: translate3d(0, -4px, 0) scale(1) rotate(-8deg); }
              45% { opacity: 1; transform: translate3d(-12px, -36px, 0) scale(1.1) rotate(12deg); }
              100% { opacity: 0; transform: translate3d(0, -150px, 0) scale(0.6) rotate(28deg); }
            }
          `}</style>
          <div
            style={{
              position: "absolute",
              inset: 0,
              zIndex: 34,
              pointerEvents: "none",
              overflow: "hidden",
            }}
          >
            {bananaParticles.map((particle) => (
              <span
                key={particle.id}
                style={{
                  position: "absolute",
                  left: `${particle.leftPct}%`,
                  top: `${particle.topPct}%`,
                  display: "inline-block",
                  fontSize: `${particle.sizeRem}rem`,
                  animationName: "bananaDanceBurst",
                  animationDuration: `${particle.durationMs}ms`,
                  animationDelay: `${particle.delayMs}ms`,
                  animationTimingFunction: "cubic-bezier(0.2, 0.9, 0.25, 1)",
                  animationFillMode: "forwards",
                  willChange: "transform, opacity",
                  filter: "drop-shadow(0 3px 8px rgba(0,0,0,0.45))",
                }}
              >
                {particle.glyph}
              </span>
            ))}
          </div>
        </>
      )}

      <OverworldHud
        snapshotEntities={snapshotEntities}
        localPlayerId={localPlayerId}
        sessionBootstrap={sessionBootstrap}
        connectionState={connectionState}
        serverTick={serverTick}
        serverMetrics={serverMetrics}
        nativeActivePlayerCount={nativeActivePlayerCount}
        sessionPlayers={sessionPlayers}
        buildStats={buildStats}
        comboResult={comboResult}
        buildBusy={buildBusy}
        nativeUiFrame={nativeUiFrame}
        onSetClass={handleSetClass}
        onApplyPreset={handleApplyPreset}
        onEquipGear={handleEquipGear}
        onEvaluateCombo={handleEvaluateCombo}
      />
    </div>
  );
}
