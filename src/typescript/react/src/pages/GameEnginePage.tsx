function routeToSessionRoom(replace = false): void {
  if (typeof window === "undefined") {
    return;
  }

  if (replace) {
    window.history.replaceState({}, "", "/session-room");
  } else {
    window.history.pushState({}, "", "/session-room");
  }
  window.dispatchEvent(new PopStateEvent("popstate"));
}

