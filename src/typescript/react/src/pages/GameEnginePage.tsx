/* Emscripten module factory type — generated by MODULARIZE=1 */
type BananaEngineModule = {
  ccall: (name: string, returnType: string, argTypes: string[], args: unknown[]) => unknown;
  cwrap: (name: string, returnType: string, argTypes: string[]) => (...args: unknown[]) => unknown;
};

type WorldPosition = {
  x: number;
  z: number;
};

type DancingBananaParticle = {
  id: number;
  leftPct: number;
  topPct: number;
  durationMs: number;
  delayMs: number;
  sizeRem: number;
  glyph: string;
};

const PBJ_PICKUP_WORLD_POSITION: WorldPosition = { x: 1.8, z: 1.6 };
const PBJ_PICKUP_COLLECT_RADIUS = 1.75;

function resolveFallbackEngineVersion(): string {
  const env = (import.meta as { env?: Record<string, string | undefined> }).env;
  const configured = env?.VITE_ENGINE_ASSET_VERSION;
  if (configured) return configured;
  if (env?.DEV) return `dev-${Date.now()}`;
  return "unknown";
}

function isWebGL2Available(): boolean {
  try {
    const probeCanvas = document.createElement("canvas");
    const context = probeCanvas.getContext("webgl2");
    return context !== null;
  } catch {
    return false;
  }
}

